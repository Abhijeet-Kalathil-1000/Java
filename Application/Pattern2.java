import java.lang.*;
import java.util.Scanner;

//////////////////////////////////////////////////////////////////////////////////////////////
//  Class with Pattern Logic
//////////////////////////////////////////////////////////////////////////////////////////////
class NumPatt
{
//////////////////////////////////////////////////////////////////////////////////////////////
//      Charachteristics
//////////////////////////////////////////////////////////////////////////////////////////////

    public int iNo;

//////////////////////////////////////////////////////////////////////////////////////////////
//      Behaviours
//////////////////////////////////////////////////////////////////////////////////////////////

    //Patameterized COnstructor
    NumPatt(int iValue)
    {
        this.iNo = iValue;
    }

//////////////////////////////////////////////////////////////////////////////////////////////
//
//     Method Name        :     NumPattern
//     Input              :     Integer (Via Patameterized COnstructor)  
//     Output             :     NA
//     Created by         :     Abhijeet Kalathil
//     Created Date       :     01/06/2022
//
//////////////////////////////////////////////////////////////////////////////////////////////

    public void NumPattern()
    {
        int iCnt1 = 0 ,iCnt2 = 0,DigCntr = 1,iSize = 1;

        for(iCnt1=1;iCnt1<=iNo;iCnt1++)
        {  
            DigCntr = iSize;
            for(iCnt2=1;iCnt2<=iNo;iCnt2++)
            {
                if(DigCntr > iNo)
                {
                    DigCntr = 1;
                }
                System.out.print(DigCntr+"\t");
                DigCntr++;
            }
            System.out.println();
            iSize++;
        }
    }
}

